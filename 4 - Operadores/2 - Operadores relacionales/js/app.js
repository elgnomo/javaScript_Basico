"use strict"
//Operadores relacionales

var valorUno = 1;
var valorDos = 2;

// mayor que >
var mayorQue = valorUno > valorDos;
console.log("Es " + valorUno + " mayor que " + valorDos + ": " + mayorQue);

// menor que <
var menorQue = valorUno < valorDos;
console.log("Es " + valorUno + " menor que " + valorDos + ": " + menorQue);

// mayor o igual que >=
var mayorOIgualQue = valorUno >= valorDos;
console.log("Es " + valorUno + " mayor o igual que " + valorDos + ": " + mayorOIgualQue);

// menor o igual que <=
var menorOIgualQue = valorUno <= valorDos;
console.log("Es " + valorUno + " menor o igual que " + valorDos + ": " + menorOIgualQue);

// igual que ==
var igualQue = valorUno == valorDos;
console.log("Es " + valorUno + " igual que " + valorDos + ": " + igualQue);

// no es igual que o es diferente que !=
var noEsIgualQue = valorUno != valorDos;
console.log("Es " + valorUno + " no es igual o es diferente que " + valorDos + ": " + noEsIgualQue);