"use strict"

//Operadores aritméticos

var valorUno = 1;
var valorDos = 2;

// Suma +
var suma = valorUno + valorDos;
console.log('La suma de ' + valorUno + ' + ' + valorDos + ' es igual a: ', suma);

// Resta -
var resta = valorUno - valorDos;
console.log('La resta de ' + valorUno + ' - ' + valorDos + ' es igual a: ', resta);

// Multiplicacion *
var multiplicacion = valorUno * valorDos;
console.log('La multiplicación de ' + valorUno + ' * ' + valorDos + ' es igual a: ', multiplicacion);

// Division /
var division = valorUno / valorDos;
console.log('La división de ' + valorUno + ' / ' + valorDos + ' es igual a: ', division);

// residuo %
var modulo = valorUno % valorDos;
console.log('El módulo o residuo de ' + valorUno + ' % ' + valorDos + ' es igual a: ', modulo);

// incremento ++
var incremento = valorUno;
incremento++;

//incremento = incremento+1
console.log('El incremento ++ de ' + valorUno + ' es igual a: ', incremento);

// decremento --
var decremento = valorUno;
decremento--;

//decremento = decremento -1
console.log('El decremento -- de ' + valorUno + ' es igual a: ', decremento);